class Books {
  String title;
  String author;
  String year;
  String language;
  String publisher;
  String genre;
  int pages;
  int totalCopies;
  int copiesAvailable;
  Books({
    required this.title,
    required this.author,
    required this.year,
    required this.language,
    required this.publisher,
    required this.genre,
    required this.pages,
    required this.totalCopies,
    required this.copiesAvailable,
  });
}
